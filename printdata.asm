	IN AL,0x92
	AND AL,0
	MOV DX,0x92
	OUT DX,AL
	
	XOR AX,AX
	CLI
	XOR DI,DI
	MOV DS,AX
	MOV ES,AX
	MOV SS,AX
	MOV SP,0x7000
	STI
	
	MOV CX,0
	
GET_OFSET:
	POP BX
	RET
MAIN_ATCK:
	MOV AX,0xB800
	MOV ES,AX

