	MOV WORD DX,[CX:DS]
	OR DX,0x20
	MOV WORD [ES:DI],DX
	ADD DI,2
	MOV BYTE [ES:DI],0x20
	INC CX
	CMP CX,0xFFFF
	JZ HALTPROCESSOR

	JMP LP
HALTPROCESSOR:
	CLI
	HLT	

