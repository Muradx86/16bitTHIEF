bits 16
org 0x7c00
section .text
global ATCK

STRA:
	JMP START

PRINT16:
	LODSB
	CMP AL,0
	NOP
	NOP
	NOP
	NOP
	NOP
	NOP
	NOP
	NOP
	MOV AH,0xE	
	INT 0x10
	JMP .R

.R:
	RET

START:
	CLI
	;DISABLE A20 FOR 8086 MODE LIMITED RAM
	IN AL,0x92
	AND AL,0
	OUT 0x92,AL
	
	XOR EAX,EAX
	MOV ES,AX
	MOV SS,AX
	MOV SP,0x7C00

	MOV CX,0
	MOV EBX,0xB8000
.LOOP:	
	MOV BYTE [ROB],AL
	MOV SI,ROB
	CLD
	CALL PRINT16

	INC CX
	CMP CX,0xFFFF
	JZ HALT
	JMP .LOOP

HALT:
	HLT
	

ROB DB ' ',13,10,0
times 510-($-$$) db 0
DW 0xAA55
