bits 16
org 0x7c00
section .text
global ATCK

STRA:
	JMP LP

PRINT16:
	LODSB
	CMP AL,0
	JZ .R
	MOV AH,0xE
	INT 0x10
	JMP PRINT

.R:
	RET

START:
	CLI
	IN AL,0x92
	OR AL,2
	OUT 0x92,AL
	XOR EAX,EAX
	MOV SS,AX
	MOV SP,0x7C00

	MOV EBX,0xB8000
.LOOP:
	INC EAX				;MY PROCESSOR IS X86-64, SO IT CAN ACCESS 32-BIT REG
	CLD
	MOV SI,AX
	MOV BYTE [EBX],ROB
	INC EBX
	MOV [EBX],0x20
	CMP EAX,0xFFFF
	JMP .LOOP

ROB DB ' ',13,10,0
