BITS 16
ORG 0x7C00
section .bss
COLD_BUF: RESB 16

section .text
COLD_MAIN:
	CLI
	XOR EAX,EAX
	MOV DS,AX
	MOV ES,AX
	MOV SS,AX
	MOV SP,0x9000

	MOV BX,0
	CLD

COLD_LOOP:
	MOV WORD SI,[BX]
	MOV WORD [COLD_BUF],SI
	MOV SI,COLD_BUF
	CALL COLD_BIOS_PRINT
	INC BX
	CMP BX,0xFFFF
	JZ COLD_HALT
	JMP COLD_LOOP

COLD_BIOS_PRINT:
	LODSB
	MOV AH,0xE
	INT 0x10
	RET
		
COLD_HALT:
	HLT
	JMP COLD_HALT
TIMES 510-($-$$) DB 0
DW 0xAA55
